public class Libro
{
    private String titulo;
    private String autor;
    
    public Libro(String iniTitulo, String iniAutor)
    {
        titulo=iniTitulo;
        autor=iniAutor;
    }
    
    public String dimeAutor()
    {
        return autor;
    }
    
    public String dimeTitulo()
    {
        return titulo;
    }
    
    
}
    
    
    